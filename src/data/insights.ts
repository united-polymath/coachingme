export function insightBySlug(slug: string) {
  return INSIGHTS.find((i) => i.slug === slug);
}

export function insightTypeLabel(type: InsightType): string {
  switch (type) {
    case 'event':
      return 'EVENT';
    case 'report':
      return 'REPORT';
    case 'interview':
      return 'INTERVIEW';
    case 'article':
    default:
      return 'ARTICLE';
  }
}

export function insightDisplayCategory(insight: Insight): string {
  if (insight.type === 'article' && insight.category) return insight.category;
  return insightTypeLabel(insight.type);
}
